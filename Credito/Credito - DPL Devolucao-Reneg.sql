WITH DEVOLUCAO AS (
    SELECT /*+ MATERIALIZE */
    N.ESTABORIGEM,
    N.SEQNOTAORIGEM,
    SUM(N.QUANTIDADE) AS QTDDEVOL
    FROM NFITEMAPARTIRDE N
    INNER JOIN NFCAB NF ON 
        NF.ESTAB = N.ESTAB
        AND NF.SEQNOTA = N.SEQNOTA
    INNER JOIN NFCFG ON NFCFG.NOTACONF = NF.NOTACONF
    INNER JOIN NATOPERACAO NAT ON
        NAT.ENTRADASAIDA = NFCFG.ENTRADASAIDA
        AND NAT.NATUREZADAOPERACAO = NFCFG.NATUREZADAOPERACAO
    WHERE NAT.TIPODCTO = 'D'
    GROUP BY N.ESTABORIGEM,N.SEQNOTAORIGEM
),
NOTAS AS (
    SELECT /*+ MATERIALIZE */
    NFCAB.ESTAB,
    NFCAB.SEQNOTA,
    NFCABAGRFIN.SEQPAGAMENTO,
    NFCAB.DTEMISSAO,
    SUM(NFITEM.QUANTIDADE) AS QUANTIDADE,
    DEVOLUCAO.QTDDEVOL
    FROM NFCAB

    INNER JOIN NFCFG ON NFCFG.NOTACONF = NFCAB.NOTACONF
    INNER JOIN NFCFG_U ON NFCFG_U.NOTACONF = NFCAB.NOTACONF
    INNER JOIN U_TIPOOP ON U_TIPOOP.U_TIPOOP_ID = NFCFG_U.U_TIPOOP_ID
    INNER JOIN NFCABAGRFIN ON
        NFCABAGRFIN.ESTAB = NFCAB.ESTAB
        AND NFCABAGRFIN.SEQNOTA = NFCAB.SEQNOTA
    INNER JOIN NFITEM ON
        NFITEM.ESTAB = NFCAB.ESTAB
    AND NFITEM.SEQNOTA = NFCAB.SEQNOTA

    INNER JOIN DEVOLUCAO ON
        DEVOLUCAO.ESTABORIGEM = NFITEM.ESTAB
    AND DEVOLUCAO.SEQNOTAORIGEM = NFITEM.SEQNOTA

    where
    NVL(NFCAB.STATUS,'X') <> 'C'
    AND U_TIPOOP.TIPOOP IN ('V','VF')
    GROUP BY NFCAB.ESTAB,NFCAB.SEQNOTA,NFCAB.DTEMISSAO,DEVOLUCAO.QTDDEVOL,NFCABAGRFIN.SEQPAGAMENTO

    HAVING 
    SUM(NFITEM.QUANTIDADE) - NVL(DEVOLUCAO.QTDDEVOL, 0) = 0
    
),
DPL_BX AS (
    SELECT EMPRESA,DUPREC,SEQRECBTO,(VALOR) AS VALOR FROM PRDUPRED
    UNION ALL
    SELECT EMPRESA,DUPREC,SEQRECBTO,(VLRCHEQREC) AS VALOR FROM PRDURECH
    UNION ALL
    SELECT EMPRESA,DUPREC,SEQRECBTO,VALOR FROM PRDUREDUP
    UNION ALL
    SELECT EMPRESA,DUPREC,SEQRECBTO,VALOR FROM PRDURECAR 
    UNION ALL
    SELECT EMPRESA,DUPREC,SEQRECBTO,VALOR FROM PRDUREOUT 
    UNION ALL
    SELECT EMPRESA,DUPREC,SEQRECBTO,VALOR FROM PRDURECM WHERE TROCO = 'N'
),
BAIXAS_DPL AS (
    SELECT EMPRESA,DUPREC,SEQRECBTO,SUM(VALOR) AS VALOR FROM DPL_BX
    GROUP BY EMPRESA,DUPREC,SEQRECBTO
),
AGR AS  (
    SELECT /*+ MATERIALIZE */
    AGRFINDUPREC.ESTAB,
    AGRFINDUPREC.SEQPAGAMENTO,
    AGRFINDUPREC.DUPREC
    FROM AGRFINDUPREC
    INNER JOIN NOTAS ON
        NOTAS.ESTAB = AGRFINDUPREC.ESTAB
        AND NOTAS.SEQPAGAMENTO = AGRFINDUPREC.SEQPAGAMENTO
),
BX AS (
    SELECT /*+ MATERIALIZE */
    PR.EMPRESA,
    PR.DUPREC,
    PR.DTRECBTO,
    --PR.TIPOREC,
    PR.NRORECIBO,
    PR.ESTABRECIBO,
    PR.SEQRECBTO,
    DPL_BX.VALOR AS VLR
    --PR.VALOR AS VLR
    FROM PRDUPREC PR
    INNER JOIN DPL_BX ON
        DPL_BX.EMPRESA = PR.EMPRESA
        AND DPL_BX.DUPREC = PR.DUPREC
        AND DPL_BX.SEQRECBTO = PR.SEQRECBTO
    WHERE PR.TIPOREC IN ('R')
),
BAIXAS AS (
    SELECT
    PP.EMPRESA,
    PP.DUPREC,
    PP.DTRECBTO,
    SUM(PP.VLR) AS VLR
    --SUM(CASE WHEN PP.TIPOREC = 'R' THEN (PP.VALOR) ELSE 0 END) VLR
    /*SUM(CASE WHEN PP.TIPOREC = 'J' THEN SUM(PP.VALOR) ELSE 0 END) JUROS,
    SUM(CASE WHEN PP.TIPOREC = 'D' THEN SUM(PP.VALOR) ELSE 0 END) DESCONT*/
    FROM BX PP

    --WHERE PP.TIPOREC IN ('R')
    GROUP BY PP.EMPRESA,PP.DUPREC,PP.DTRECBTO
),
DPL as (
    SELECT /*+ MATERIALIZE */
    EMPRESA,
    DUPREC,
    DTEMISSAO,
    DTVENCTO,
    CLIENTE,
    SEQENDERECO,
    SITUACAO,
    VALOR,
    JUROS,
    DESCONTO
    FROM PDUPREC
),
EMP AS (
    SELECT
    ESTAB,
    CASE 
        WHEN OUTROS = 'S' THEN 'O'
        WHEN INSUMOS = 'S' THEN 'I'
        WHEN GRAOS = 'S' THEN 'C'
        ELSE 'O'
    END TIPOESTAB
    FROM U_TEMPRESA
),
cmv as (
select distinct
    BX.EMPRESA,
    BX.DUPREC,
    BX.SEQRECBTO
    from contamovlanac cl
    inner join contamovlan c on
        c.estab = cl.estabacertado
        and c.numerocm = cl.numerocm
        and c.seqcm = cl.seqacertada
    inner join contamovtp tp on
        tp.tipo = c.tipo
        and tp.DEBCRED = c.DEBCRED
    inner join BX on
        BX.ESTABRECIBO = cl.ESTABRECIBO
        AND BX.NRORECIBO = cl.NRORECIBO
    where
    tp.debcred = 'C' and tp.RECIBO = 'S'
    AND TP.DESCRICAO LIKE '%DEVOL%'
)
