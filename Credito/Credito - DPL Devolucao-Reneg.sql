AND 
    NOT EXISTS (
    SELECT 1 FROM RENEGOCIACAODOCTOS
    WHERE RENEGOCIACAODOCTOS.ESTAB = P.EMPRESA
    AND RENEGOCIACAODOCTOS.DUPREC = P.DUPREC
    )
    and
    NOT EXISTS (
        SELECT 1 FROM AGR
        WHERE AGR.ESTAB = P.EMPRESA
        AND AGR.DUPREC = P.DUPREC
    )
    
    AND NOT EXISTS (
    SELECT
    1
    FROM CMV WHERE CMV.EMPRESA = BX.EMPRESA
    AND CMV.DUPREC = BX.DUPREC
    AND CMV.SEQRECBTO = BX.SEQRECBTO
