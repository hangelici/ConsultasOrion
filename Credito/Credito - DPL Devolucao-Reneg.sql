    OR PSITUACA.DESCRICAO LIKE '%MILHO%' OR PSITUACA.DESCRICAO LIKE '%FEIJAO%'
    OR PSITUACA.DESCRICAO LIKE '%SORGO%' OR PSITUACA.DESCRICAO LIKE '%UVA%'
    THEN 'Baixa em Grãos'
    else 'Baixa Financeira'
END TIPORECEB
,NFCAB.NOTA
