SALDO_FINAL AS (
---------- ROMANEIO EM ABERTO
SELECT
'ROMA PENDENTE' AS TIPO,
R.ESTAB,
0 AS SEQNOTA,
0 AS NOTACONF,
R.NUMEROCM,
R.ITEM,
R.ROMANEIO,
ITEMAGROESTAB.COTACAO,
ITEMCOTITE.FIXACAO,
(R.PESOLIQUIDO - NVL(TRANSF.QTD_TRANSF,0)) - 
        CASE
            WHEN R.REACOBFINLEITURA = 0 AND NVL(PAGAMENTOS_ROMA.QTDEPAGO,0) > 0 THEN PAGAMENTOS_ROMA.QTDEPAGO
            WHEN R.REACOBFINLEITURA = 0 AND NVL(PAGAMENTOS_ROMA.QTDEPAGO,0) = 0 THEN
            DIVIDE(    
                DIVIDE(
                    (NVL(R.PESOTOTAL,0) - NVL(R.TARA,0)) * NVL(UMIDADE_ROMA.VALOR_UMIDADE,0),
                    60),
            ITEMCOTITE.FIXACAO
            )*60
        ELSE 0 END
 AS SALDO
    
FROM ROMA_ABERTO R

LEFT JOIN OS_ROMA_UMIDADE UMIDADE_ROMA ON
    R.ESTAB = UMIDADE_ROMA.ESTAB
    AND R.NUMEROCM = UMIDADE_ROMA.NUMEROCM
    AND R.ROMANEIO = UMIDADE_ROMA.ROMANEIO
    AND R.ENTRADASAIDA = UMIDADE_ROMA.ENTRADASAIDA

