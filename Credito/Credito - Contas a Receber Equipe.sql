    SUM(CASE
        WHEN NATOPERACAO.TIPODCTO = 'D' AND NFCFG.NOTACONF NOT IN (205,250,251) THEN N.QUANTIDADE
        ELSE 0 
    END) QTD_DEVOL,
    SUM(CASE WHEN NATOPERACAO.TIPODCTO = 'X' THEN N.QUANTIDADE ELSE 0 END) QTD_FIX,
    SUM(CASE
        WHEN NATOPERACAO.TIPODCTO = 'D' AND NFCFG.NOTACONF IN (205,250,251) THEN N.QUANTIDADE
        ELSE 0 
    END) QTD_QUEBRA
