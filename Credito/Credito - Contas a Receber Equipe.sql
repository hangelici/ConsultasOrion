    ITEMCOTITE.COTACAO              = ITEMCOTCAB.COTACAO AND
    R.DTEMISSAO                  BETWEEN ITEMCOTITE.DTCOTACAO AND ITEMCOTITE.DTCOTACAOFIN
    
